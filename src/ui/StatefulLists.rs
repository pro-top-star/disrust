use tui::widgets::ListState;

#[derive(Debug)]
pub struct GuildList<Guild> {
    pub state: ListState,
    pub items: Vec<Guild>,
}

impl<Guild> GuildList<Guild> {
    pub fn with_items(items: Vec<Guild>) -> GuildList<Guild> {
        GuildList {
            state: ListState::default(),
            items,
        }
    }

    pub fn next(&mut self) {
        let i = match self.state.selected() {
            Some(i) => {
                if i >= self.items.len() - 1 {
                    0
                } else {
                    i + 1
                }
            }
            None => 0,
        };
        self.state.select(Some(i));
    }

    pub fn previous(&mut self) {
        let i = match self.state.selected() {
            Some(i) => {
                if i == 0 {
                    self.items.len() - 1
                } else {
                    i - 1
                }
            }
            None => 0,
        };
        self.state.select(Some(i));
    }

    pub fn unselect(&mut self) {
        self.state.select(None);
    }
}

